    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Conexion cc = new Conexion();
        Connection cn = cc.conexion();

        int CodiRuta = Integer.parseInt(jTextField1.getText());
        int CodViaje = Integer.parseInt(jTextField2.getText());
        int Avendidos = Integer.parseInt(jTextField7.getText());
        float Precio = Float.parseFloat(jTextField6.getText());
        float Tot = Float.parseFloat(jTextField8.getText());

        String sql = "INSERT INTO BOLETOS VALUES(?,?,?,0,?,?);";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setInt(1, CodiRuta);
            pst.setInt(2, CodViaje);
            pst.setInt(3, Avendidos);
            pst.setFloat(4, Precio);
            pst.setFloat(5, Tot);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro Guardado");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error de Registro");
            Logger.getLogger(Boletos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

